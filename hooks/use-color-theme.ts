export function useColorTheme(colorTheme: string = 'neutral'): void {
  useEffect(() => {
    const applyTheme = (): void => {
      const root = document.documentElement
      const isDark = document.documentElement.classList.contains('dark')

      // Apply the selected theme (including neutral)
      const theme =
        colorThemes[colorTheme as keyof typeof colorThemes] ||
        colorThemes.neutral
      const colors = isDark ? theme.dark : theme.light

      // Apply all theme properties to make themes fully visible
      const themeProperties = [
        '--background',
        '--foreground',
        '--card',
        '--card-foreground',
        '--popover',
        '--popover-foreground',
        '--primary',
        '--primary-foreground',
        '--secondary',
        '--secondary-foreground',
        '--muted',
        '--muted-foreground',
        '--accent',
        '--accent-foreground',
        '--destructive',
        '--destructive-foreground',
        '--border',
        '--input',
        '--ring',
      ] as const

      // Apply all theme properties
      themeProperties.forEach((property) => {
        const value = colors[property as keyof typeof colors]
        if (value) {
          root.style.setProperty(property, value)
        }
      })

      // Notify listeners that color theme CSS variables changed
      setTimeout(() => {
        document.dispatchEvent(new Event('color-theme-change'))
      }, 0)
    }

    // Apply theme immediately
    applyTheme()

    // Add a listener for theme changes from next-themes
    const observer = new MutationObserver((mutations) => {
      mutations.forEach((mutation) => {
        if (
          mutation.type === 'attributes' &&
          mutation.attributeName === 'class'
        ) {
          // Only reapply if the class change is related to dark/light mode
          const target = mutation.target as HTMLElement
          if (
            target.classList.contains('dark') ||
            target.classList.contains('light')
          ) {
            applyTheme()
          }
        }
      })
    })

    observer.observe(document.documentElement, {
      attributes: true,
      attributeFilter: ['class'],
    })

    return (): void => observer.disconnect()
  }, [colorTheme])
}
